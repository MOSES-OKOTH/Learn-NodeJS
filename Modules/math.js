function sum(a,b){
    return a+b;
}

function difference(a,b){
    return a-b;
}

function divide(a,b){
    return a/b;
}

function multiply(a,b){
    return a*b;
}

function modulus(a,b){
    return a%b;
}


/* Exporting all the functions */
module.exports = {sum, difference, divide, multiply, modulus}